# Function to create or update a label
create_or_update_label() {
  local name="$1"
  local color="$2"
  local description="$3"

  if gh label create "$name" --color "$color" --description "$description" --force 2>/dev/null; then
    echo "âœ“ Created/updated label: $name"
  else
    echo "âš  Failed to create label: $name (may already exist with different settings)"
    # Try to update existing label instead
    if gh label edit "$name" --color "$color" --description "$description" 2>/dev/null; then
      echo "âœ“ Updated existing label: $name"
    else
      echo "âš  Could not update label: $name"
    fi
  fi
}

